# ---------------------------------------------------
# BAD WORDS
# ---------------------------------------------------

BAD_WORDS = [

    "hook:",
    "script:",
    "cta:",
    "title:",
    "शीर्षक:",
    "स्क्रिप्ट:",
    "like share",
]

# ---------------------------------------------------
# GET TODAY TOPIC
# ---------------------------------------------------

def get_today_topic():

    current_day = datetime.today().strftime("%A")

    if current_day not in DAY_TOPICS:

        current_day = "Monday"

    return random.choice(
        DAY_TOPICS[current_day]
    )

# ---------------------------------------------------
# CHECK HINDI
# ---------------------------------------------------

def is_hindi(text):

    hindi_chars = re.findall(
        r'[\u0900-\u097F]',
        text
    )

    total_chars = re.findall(
        r'[A-Za-z\u0900-\u097F]',
        text
    )

    if len(total_chars) == 0:
        return False

    return (
        len(hindi_chars) / len(total_chars)
    ) > 0.92

# ---------------------------------------------------
# CLEAN SCRIPT
# ---------------------------------------------------

def clean_script(script):

    script = script.replace("**", "")

    patterns = [

        r'HOOK\s*:',
        r'SCRIPT\s*:',
        r'CTA\s*:',
        r'शीर्षक\s*:',
        r'स्क्रिप्ट\s*:',
    ]

    for pattern in patterns:

        script = re.sub(
            pattern,
            '',
            script,
            flags=re.IGNORECASE
        )

    script = script.replace("\n", " ")

    script = re.sub(
        r'\s+',
        ' ',
        script
    )

    return script.strip()

# ---------------------------------------------------
# VALIDATE
# ---------------------------------------------------

def validate_script(script):

    if len(script) < 220:
        return False

    if not is_hindi(script):
        return False

    for word in BAD_WORDS:

        if word.lower() in script.lower():
            return False

    return True

